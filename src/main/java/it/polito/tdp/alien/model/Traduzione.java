package it.polito.tdp.alien.model;

public class Traduzione {

	String ParolaAliena; 
	String TraduzioneItaliana;
		
	
	public Traduzione(String parolaAliena, String traduzioneItaliana) {
		super();
		ParolaAliena = parolaAliena;
		TraduzioneItaliana = traduzioneItaliana;
	}

	public String getParolaAliena() {
		return ParolaAliena;
	}

	public String getTraduzioneItaliana() {
		return TraduzioneItaliana;
	} 
		
		
		
		
	}

